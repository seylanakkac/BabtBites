/// Linear-interpolated reference value at fractional [ageMonths] (0..24).
double whoValueAt(List<double> series, double ageMonths) {
  if (ageMonths <= 0) return series.first;
  if (ageMonths >= series.length - 1) return series.last;
  final i = ageMonths.floor();
  final frac = ageMonths - i;
  return series[i] + (series[i + 1] - series[i]) * frac;
}
